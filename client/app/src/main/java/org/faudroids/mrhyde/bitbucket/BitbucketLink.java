package org.faudroids.mrhyde.bitbucket;

/**
 * Bitbucket Link object.
 */
public class BitbucketLink {

  private String href;
  private String name;

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getHref() {
    return href;
  }

  public void setHref(String href) {
    this.href = href;
  }
}
